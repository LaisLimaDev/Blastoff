"""  Uma partida de futebol iniciou na hora A e terminou na hora B. Faça um algoritmo que
calcule o tempo que durou a partida. """



inicio = float(input("Informe o horário de início da partida: "))
termino = float(input("Informe o horário de término da partida: "))

duracao =(termino-inicio)
print("O tempo de duração da partida foi: ", duracao)